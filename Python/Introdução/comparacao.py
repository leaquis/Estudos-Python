x = int(input("Digite um numero: "))

if x == 10:
    print("Seu numero é igual a 10")
else:
    print("Seu numero não é igual a 10")

if x != 5:
    print("Seu numero é diferente de 5")
else:
    print("Seu numero não é diferente de 5")

if x > 3:
    print("Seu numero é maior que 3")
else:
    print("Seu numero não é maior que 3")

if x < 20:
    print("Seu numero é menor que 20")
else:
    print("Seu numero não é menor que 20")

if x >= 7:
    print("Seu numero é maior ou igual a 7")
else:
    print("Seu numero não é maior ouigual a 7")

if x <= 35:
    print("Seu numero é menor ou igual a 35")
else:
    print("Seu numero não é menor ou igual a 35")
